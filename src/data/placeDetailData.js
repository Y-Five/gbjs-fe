export const getPlaceById = (id) => 
  placeDetailData.find(place => place.id === parseInt(id));

export const getPlacesByCategory = (category) => 
  category === "전체" 
    ? placeDetailData 
    : placeDetailData.filter(place => place.category === category);

export const getPlacesWithVoiceGuide = () => 
  placeDetailData.filter(place => place.hasVoiceGuide);

export const getPlacesByPopularity = (limit) => {
  const sorted = [...placeDetailData].sort((a, b) => b.popularity - a.popularity);
  return limit ? sorted.slice(0, limit) : sorted;
};

export const getPlacesByDistance = (limit) => {
  const sorted = [...placeDetailData].sort((a, b) => a.distance - b.distance);
  return limit ? sorted.slice(0, limit) : sorted;
};

export const getPlacesWithinDistance = (maxDistance) => 
  placeDetailData.filter(place => place.distance <= maxDistance);

export const searchPlaces = (query) => {
  const lowerQuery = query.toLowerCase();
  return placeDetailData.filter(place => 
    place.name.toLowerCase().includes(lowerQuery) ||
    place.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    place.description.toLowerCase().includes(lowerQuery)
  );
};